async function CountTotal1(Cuentas) {
    let totalcuentas = Cuentas.reduce((init, nextvalue)=>{  
        return init + parseInt(nextvalue.SALDO)
    },0);
    return totalcuentas;
}

async function CountTotal2(Cuentas) {
    let totalcuentas = Cuentas.reduce((init, nextvalue)=>{  
        return init + parseInt(nextvalue.CAJAS)
    },0);
    return totalcuentas;
}

